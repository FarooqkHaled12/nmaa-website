// Language Manager
class LanguageManager {
    constructor() {
        this.currentLang = localStorage.getItem('nmaa-language') || 'ar';
        this.init();
        window.languageManager = this;
    }

    init() {
        // Set initial language
        this.setLanguage(this.currentLang);
        this.refreshListeners();
    }

    refreshListeners() {
        // Add event listeners to language selectors
        const desktopLangSelect = document.getElementById('languageSelect');
        const mobileLangSelect = document.querySelector('.mobile-lang-select');

        if (desktopLangSelect) {
            desktopLangSelect.value = this.currentLang;
            // Remove existing to avoid duplicates if called multiple times
            desktopLangSelect.onchange = (e) => {
                this.setLanguage(e.target.value);
            };
        }

        if (mobileLangSelect) {
            mobileLangSelect.value = this.currentLang;
            mobileLangSelect.onchange = (e) => {
                this.setLanguage(e.target.value);
            };
        }
    }

    setLanguage(lang) {
        this.currentLang = lang;
        localStorage.setItem('nmaa-language', lang);

        // Update HTML attributes
        document.documentElement.lang = lang;
        document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';

        // Update language selectors (visual sync)
        const desktopLangSelect = document.getElementById('languageSelect');
        const mobileLangSelect = document.querySelector('.mobile-lang-select');

        if (desktopLangSelect) desktopLangSelect.value = lang;
        if (mobileLangSelect) mobileLangSelect.value = lang;

        // Translate all elements with data-translate attribute
        this.translatePage();

        // Update CSS for RTL/LTR
        document.body.classList.toggle('rtl', lang === 'ar');
        document.body.classList.toggle('ltr', lang === 'en');
    }

    translatePage() {
        const elements = document.querySelectorAll('[data-translate]');
        elements.forEach(element => {
            const key = element.getAttribute('data-translate');
            const translation = translations[this.currentLang]?.[key];
            if (translation) {
                if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                    if (element.hasAttribute('placeholder')) {
                        element.placeholder = translation;
                    } else {
                        element.textContent = translation;
                    }
                } else if (/<[a-z][\s\S]*>/i.test(translation)) {
                    element.innerHTML = translation;
                } else {
                    element.textContent = translation;
                }
            }
        });
    }

    getTranslation(key) {
        return translations[this.currentLang]?.[key] || key;
    }
}

// Initialize language manager when DOM is ready
document.addEventListener('DOMContentLoaded', () => {
    if (!window.languageManager) {
        new LanguageManager();
    }
});

// Export for use in other scripts
if (typeof module !== 'undefined' && module.exports) {
    module.exports = { translations, LanguageManager };
}
